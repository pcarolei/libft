int	ft_get_number_len(int n)
{
	int	cnt;

	cnt = 0;
	if (n == 0)
		return (1);
	if (n < 0)
		n = n * (-1);
	while (n > 0)
	{
		n = n / 10;
		cnt++;
	}
	return (cnt);
}
